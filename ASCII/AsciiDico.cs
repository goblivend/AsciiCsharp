        {
            StreamReader reader = new StreamReader(path);
            string s = "";
            int i = 0;
            while ((s = reader.ReadLine()) != null)
            {
                string newi = "{";
                newi += i;
                newi += ", \"";
                newi += s;
                newi += "\"},";
                Console.WriteLine(newi);
                i++;
            }
            reader.Close();
        }
        
        
